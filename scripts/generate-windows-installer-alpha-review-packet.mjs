function updateRecord(dir, mutate) {
  const recordPath = path.join(dir, windowsInstallerAssetNames(version).installerRecord);
  const record = JSON.parse(fs.readFileSync(recordPath, "utf8"));
  mutate(record);
  fs.writeFileSync(recordPath, `${JSON.stringify(record, null, 2)}\n`);
  writeChecksums(dir);
}

function writeChecksums(dir) {
  const names = windowsInstallerAssetNames(version);
  const entries = expectedWindowsInstallerDownloadableAssetNames(version)
    .filter((name) => name !== names.checksums)
    .sort((a, b) => a.localeCompare(b));
  const lines = [];
  for (const name of entries) {
    const description = describeFixtureFile(dir, name);
    lines.push(`sha256  ${description.sha256.replace(/^sha256:/, "")}  ${name}`);
    lines.push(`sha512  ${description.sha512.replace(/^sha512:/, "")}  ${name}`);
  }
  fs.writeFileSync(path.join(dir, names.checksums), `${lines.join("\n")}\n`);
}

