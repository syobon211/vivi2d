function renderFixtureResults(results) {
  const lines = [
    "| Fixture | Expected | Actual | Result | Diagnostic |",
    "| --- | --- | --- | --- | --- |",
  ];
  for (const result of results) {
    lines.push(
      `| ${result.name} | ${result.expected} | ${result.actual} | ${result.ok ? "PASS" : "FAIL"} | ${escapeTableCell(result.diagnostic)} |`,
    );
  }
  return lines.join("\n");
}

function renderFileSection(relativePath) {
  const content = fs.readFileSync(resolveRepoPath(relativePath), "utf8");
  return [`### ${relativePath}`, "", "````text", content.trimEnd(), "````", ""];
}

function requireSection(text, start, end) {
  const startIndex = text.indexOf(start);
  if (startIndex < 0) throw new Error(`Missing workflow section: ${start}`);
  const endIndex = end === null ? -1 : text.indexOf(end, startIndex + start.length);
  return text.slice(startIndex, endIndex < 0 ? text.length : endIndex);
}

function parseWorkflowSteps(jobSection) {
  const steps = [];
  let current = null;
  for (const line of jobSection.split(/\r?\n/)) {
    if (/^\s{6}-\s+/.test(line)) {
      if (current) steps.push(current);
      current = [line];
      continue;
    }
    if (current) current.push(line);
  }
  if (current) steps.push(current);
  return steps.map((rawLines, index) => {
    const raw = rawLines.join("\n");
    return {
      index,
      name: extractScalarField(rawLines, "name"),
      raw,
      run: extractRun(rawLines),
      uses: extractScalarField(rawLines, "uses"),
    };
  });
}

function extractScalarField(rawLines, fieldName) {
  const fieldPattern = new RegExp(`^\\s{6}(?:-\\s+)?${fieldName}:\\s*(.*)$`);
  for (const line of rawLines) {
    const match = fieldPattern.exec(line);
    if (match) return match[1].trim().replace(/^["']|["']$/g, "");
  }
  return "";
}

function extractRun(rawLines) {
  const runPattern = /^\s*(?:-\s+)?run:\s*(.*)$/;
  const runIndex = rawLines.findIndex((line) => runPattern.test(line));
  if (runIndex < 0) return "";
  const value = runPattern.exec(rawLines[runIndex])?.[1]?.trim() ?? "";
  if (value !== "|" && value !== ">") return value;
  return rawLines
    .slice(runIndex + 1)
    .map((line) => line.replace(/^\s{10}/, ""))
    .join("\n")
    .trim();
}

function extractTopLevelEnvKeys(jobSection) {
  const stepsIndex = jobSection.indexOf("\n    steps:");
  const header = stepsIndex < 0 ? jobSection : jobSection.slice(0, stepsIndex);
  const envIndex = header.indexOf("\n    env:");
  if (envIndex < 0) return [];
  return [...header.slice(envIndex).matchAll(/^\s{6}([A-Za-z_][A-Za-z0-9_]*):/gm)].map(
    (match) => match[1],
  );
}

function describeFixtureFile(dir, name) {
  const bytes = fs.readFileSync(path.join(dir, name));
  return {
    name,
    sha256: `sha256:${crypto.createHash("sha256").update(bytes).digest("hex")}`,
    sha512: `sha512:${crypto.createHash("sha512").update(bytes).digest("hex")}`,
    sizeBytes: bytes.byteLength,
  };
}

function firstDiagnostic(result) {
  return (
    (result.stderr || result.stdout || "")
      .split(/\r?\n/)
      .map((line) => line.trim())
      .filter(Boolean)[0] ?? ""
  );
}

function escapeTableCell(value) {
  return String(value).replaceAll("|", "\\|").replace(/\s+/g, " ");
}

function slugify(value) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

function relative(filePath) {
  return path.relative(repoRoot, filePath).replaceAll("\\", "/");
}
