import { exists, gitLsFiles, readJson, readText } from "./lib/repo.mjs";

const failures = [];
const lockedMediapipeWasmPattern =
  /(^|[/\\])packages[/\\]viewer[/\\]public[/\\]vendor[/\\]mediapipe[/\\]tasks-vision-0\.10\.35[/\\]wasm[/\\]vision_wasm_(?:internal|module_internal|nosimd_internal)\.wasm$/;

function fail(message) {
  failures.push(message);
}

const runtimeWasmPackage = readJson("packages/runtime-wasm/package.json");
if (runtimeWasmPackage.private !== true) {
  fail("@vivi2d/runtime-wasm must remain private while native WASM is internal.");
}
if (runtimeWasmPackage.vivi2d?.publication !== "internal") {
  fail("@vivi2d/runtime-wasm must remain vivi2d.publication=internal.");
}

const runtimeNativePackage = readJson("packages/runtime-native/package.json");
if (runtimeNativePackage.private !== true) {
  fail(
    "@vivi2d/runtime-native must remain private before native release policy is finalized.",
  );
}
if (runtimeNativePackage.vivi2d?.publication !== "internal") {
  fail("@vivi2d/runtime-native must remain vivi2d.publication=internal.");
}

const generatedBytesPath = "packages/runtime-wasm/src/native-wasm-bytes.ts";
if (!exists(generatedBytesPath)) {
  fail(`${generatedBytesPath} is missing.`);
} else {
  const generatedBytes = readText(generatedBytesPath);
  if (
    !generatedBytes.startsWith(
      "// Generated by scripts/generate-runtime-native-wasm-module.mjs.",
    )
  ) {
    fail(`${generatedBytesPath} must carry the generated-file provenance header.`);
  }
  if (!generatedBytes.includes("VIVI_RUNTIME_NATIVE_WASM_BASE64")) {
    fail(`${generatedBytesPath} must export VIVI_RUNTIME_NATIVE_WASM_BASE64.`);
  }
}

for (const requiredPath of [
  ".cargo/config.toml",
  "scripts/generate-runtime-native-wasm-module.mjs",
  "scripts/check-runtime-native-wasm.mjs",
  "scripts/check-runtime-wasm-browser.mjs",
  ".github/workflows/runtime-wasm-browser.yml",
]) {
  if (!exists(requiredPath))
    fail(`Missing native/WASM artifact policy input: ${requiredPath}`);
}

const trackedWasm = gitLsFiles(["*.wasm", "packages/**/*.wasm"]).filter(
  (relativePath) => !lockedMediapipeWasmPattern.test(relativePath),
);
if (trackedWasm.length > 0) {
  fail(`Raw .wasm artifacts must not be tracked directly: ${trackedWasm.join(", ")}`);
}

const browserWorkflow = exists(".github/workflows/runtime-wasm-browser.yml")
  ? readText(".github/workflows/runtime-wasm-browser.yml")
  : "";
for (const needle of [
  "packages/runtime-native/**",
  "packages/runtime-wasm/**",
  "scripts/generate-runtime-native-wasm-module.mjs",
  "scripts/check-runtime-wasm-browser.mjs",
  "npx playwright install --with-deps chromium firefox webkit",
  "npm run test:runtime-wasm:browser",
]) {
  if (!browserWorkflow.includes(needle)) {
    fail(`runtime-wasm-browser workflow should include: ${needle}`);
  }
}

const nativeCheck = exists("scripts/check-runtime-native-wasm.mjs")
  ? readText("scripts/check-runtime-native-wasm.mjs")
  : "";
for (const needle of [
  "--release",
  "generate-runtime-native-wasm-module.mjs",
  "WebAssembly.validate",
  "readDefinedMemoryLimits",
  "expectedMaxMemoryPages",
  "vivi_wasm_model_load",
  "vivi_wasm_model_snapshot_json",
  "vivi_wasm_model_hit_test_json",
]) {
  if (!nativeCheck.includes(needle)) {
    fail(`check-runtime-native-wasm should validate: ${needle}`);
  }
}

const cargoConfig = exists(".cargo/config.toml") ? readText(".cargo/config.toml") : "";
if (!cargoConfig.includes("--max-memory=67108864")) {
  fail(".cargo/config.toml must bound wasm32 native runtime maximum memory.");
}

const threatModel = exists("docs/developer/security/threat-model.md")
  ? readText("docs/developer/security/threat-model.md")
  : "";
if (!threatModel.includes("Native/WASM runtime artifacts")) {
  fail(
    "docs/developer/security/threat-model.md must mention Native/WASM runtime artifact controls.",
  );
}

const releasePolicy = exists("docs/developer/quality/release-policy.md")
  ? readText("docs/developer/quality/release-policy.md")
  : "";
for (const needle of [
  "checksums are generated for every binary artifact",
  "signing or attestation mechanism is chosen",
  "sanitizer/Miri coverage is evaluated",
  "fuzz or hostile-input coverage is evaluated",
  "Native/WASM checksum and signing decisions are blockers",
]) {
  if (!releasePolicy.includes(needle)) {
    fail(`docs/developer/quality/release-policy.md must require: ${needle}`);
  }
}

if (failures.length > 0) {
  console.error("[native-artifact-policy] failed:");
  for (const failure of failures) {
    console.error(`- ${failure}`);
  }
  process.exit(1);
}

console.log("[native-artifact-policy] passed");
