import { existsSync, readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(scriptDir, "..");
const checkOnly = process.argv.includes("--check");
const wasmPath = path.join(
  root,
  "packages/runtime-native/target/wasm32-unknown-unknown/release/vivi_runtime_native_wasm.wasm",
);
const outputPath = path.join(root, "packages/runtime-wasm/src/native-wasm-bytes.ts");

const wasmBase64 = readFileSync(wasmPath).toString("base64");
const lines = wasmBase64.match(/.{1,100}/g) ?? [""];
const source = `// Generated by scripts/generate-runtime-native-wasm-module.mjs.\n// Do not edit by hand.\n\nexport const VIVI_RUNTIME_NATIVE_WASM_BASE64 =\n${lines
  .map((line, index) => `  "${line}"${index === lines.length - 1 ? ";" : " +"}`)
  .join("\n")}\n`;

if (checkOnly) {
  const current = existsSync(outputPath) ? readFileSync(outputPath, "utf8") : "";
  if (current !== source) {
    throw new Error(
      "packages/runtime-wasm/src/native-wasm-bytes.ts is stale; run node scripts/generate-runtime-native-wasm-module.mjs",
    );
  }
  console.log("[runtime-native-wasm] generated TypeScript bytes are current");
  process.exit(0);
}

writeFileSync(outputPath, source);
console.log(`[runtime-native-wasm] wrote ${path.relative(root, outputPath)}`);
